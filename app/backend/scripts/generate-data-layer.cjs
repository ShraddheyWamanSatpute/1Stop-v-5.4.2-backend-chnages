const fs = require("fs")
const path = require("path")

const ROOT = process.cwd()
const RTDB_DIR = path.join(ROOT, "app", "backend", "rtdatabase")
const DATA_DIR = path.join(ROOT, "app", "backend", "data")
const SUPABASE_DIR = path.join(ROOT, "app", "backend", "providers", "supabase")
const INVENTORY_PATH = path.join(DATA_DIR, "inventory.generated.json")
const SUMMARY_PATH = path.join(DATA_DIR, "inventory.generated.md")
const ADMIN_RTDB_DIR = path.join(ROOT, "admin", "backend", "rtdatabase")
const ADMIN_DATA_DIR = path.join(ROOT, "admin", "backend", "data")
const ADMIN_SUPABASE_DIR = path.join(ROOT, "admin", "backend", "providers", "supabase")
const ADMIN_INVENTORY_PATH = path.join(ADMIN_DATA_DIR, "inventory.generated.json")
const ADMIN_SUMMARY_PATH = path.join(ADMIN_DATA_DIR, "inventory.generated.md")

const SOURCE_DIRS = [
  path.join(ROOT, "app", "backend", "context"),
  path.join(ROOT, "app", "backend", "functions"),
  path.join(ROOT, "app", "backend", "services"),
  path.join(ROOT, "app", "frontend"),
  path.join(ROOT, "admin"),
  path.join(ROOT, "mobile"),
  path.join(ROOT, "main-site"),
  path.join(ROOT, "pos-hub"),
  path.join(ROOT, "yourstop"),
].filter((dir) => fs.existsSync(dir))

const MODULE_RE = /(?:rtdatabase|data)\/([A-Za-z0-9_-]+)/g
const EXTENSIONS = new Set([".ts", ".tsx", ".js", ".jsx", ".cjs", ".mjs"])
const GENERATED_HEADER = "// Generated by scripts/generate-data-layer.cjs."

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true })
}

function writeGeneratedIfManaged(filePath, source) {
  if (fs.existsSync(filePath)) {
    const current = fs.readFileSync(filePath, "utf8")
    if (current && !current.startsWith(GENERATED_HEADER)) {
      return
    }
  }
  fs.writeFileSync(filePath, source, "utf8")
}

function walkFiles(startDir) {
  const results = []
  if (!fs.existsSync(startDir)) return results

  for (const entry of fs.readdirSync(startDir, { withFileTypes: true })) {
    const fullPath = path.join(startDir, entry.name)
    if (entry.isDirectory()) {
      if (entry.name === "dist" || entry.name === "node_modules") continue
      results.push(...walkFiles(fullPath))
      continue
    }
    if (EXTENSIONS.has(path.extname(entry.name))) {
      results.push(fullPath)
    }
  }

  return results
}

function extractExports(source) {
  const names = []
  const seen = new Set()
  const patterns = [
    /^export\s+(?:async\s+)?function\s+([A-Za-z0-9_]+)/gm,
    /^export\s+const\s+([A-Za-z0-9_]+)/gm,
  ]

  for (const pattern of patterns) {
    let match
    while ((match = pattern.exec(source)) !== null) {
      const name = match[1]
      if (!seen.has(name)) {
        seen.add(name)
        names.push(name)
      }
    }
  }

  return names
}

function classifyExport(name) {
  if (/^use[A-Z]/.test(name)) return "hook"
  if (/^subscribe/.test(name)) return "subscription"
  if (/^(fetch|get)/.test(name)) return "read"
  if (/^(create|save|add|record|append|signIn|signUp|register|login|send|set)/.test(name)) return "write"
  if (/^(update|mark|approve|post|reverse|lock|unlock|upsert|adjust)/.test(name)) return "update"
  if (/^(delete|remove|revoke)/.test(name)) return "delete"
  if (/^(calculate|search|check|initialize|invalidate|combine|convert)/.test(name)) return "utility"
  return "other"
}

function toPosix(filePath) {
  return path.relative(ROOT, filePath).split(path.sep).join("/")
}

function createWrapperSource(moduleName, exportNames, imports) {
  const lines = [
    "// Generated by scripts/generate-data-layer.cjs. Do not edit by hand.",
    `import * as firebaseProvider from "${imports.firebaseImportPath}"`,
    `import * as supabaseProvider from "${imports.supabaseImportPath}"`,
    `import { getModuleProvider } from "${imports.registryImportPath}"`,
    "",
    "type ProviderModule = typeof firebaseProvider",
    "type ProviderFn<T extends keyof ProviderModule> = ProviderModule[T] extends (...args: infer Args) => infer Result",
    "  ? (...args: Args) => Result",
    "  : never",
    "",
    `const resolveImplementation = <T extends keyof ProviderModule>(name: T): ProviderFn<T> => {`,
    `  const provider = getModuleProvider("${moduleName}")`,
    '  const source = provider === "supabase" ? (supabaseProvider as ProviderModule) : (firebaseProvider as ProviderModule)',
    "  const implementation = source[name]",
    '  if (typeof implementation !== "function") {',
    '    throw new Error(`[data-provider] ${String(name)} is not implemented for provider "${provider}" in module "' + moduleName + '".`)',
    "  }",
    "  return implementation as ProviderFn<T>",
    "}",
    "",
  ]

  for (const exportName of exportNames) {
    lines.push(
      `export const ${exportName}: typeof firebaseProvider.${exportName} = ((...args: Parameters<typeof firebaseProvider.${exportName}>) =>`,
      `  resolveImplementation("${exportName}")(...args)) as typeof firebaseProvider.${exportName}`,
      "",
    )
  }

  return `${lines.join("\n").trim()}\n`
}

function createSupabaseStubSource(moduleName, exportNames) {
  return createSupabaseStubSourceWithImports(moduleName, exportNames, {
    firebaseImportPath: `../../rtdatabase/${moduleName}`,
    runtimeImportPath: "./runtime",
  })
}

function createSupabaseStubSourceWithImports(moduleName, exportNames, imports) {
  const lines = [
    "// Generated by scripts/generate-data-layer.cjs. Replace stubs with real Supabase implementations module by module.",
    `import type * as firebaseProvider from "${imports.firebaseImportPath}"`,
    `import { createSupabaseStub } from "${imports.runtimeImportPath}"`,
    "",
  ]

  for (const exportName of exportNames) {
    lines.push(
      `export const ${exportName}: typeof firebaseProvider.${exportName} = createSupabaseStub("${moduleName}", "${exportName}") as typeof firebaseProvider.${exportName}`,
      "",
    )
  }

  return `${lines.join("\n").trim()}\n`
}

function rewriteImports(filePath) {
  const original = fs.readFileSync(filePath, "utf8")
  const updated = original
    .replace(/backend\/rtdatabase\//g, "backend/data/")
    .replace(/\/rtdatabase\//g, "/data/")

  if (updated !== original) {
    fs.writeFileSync(filePath, updated, "utf8")
    return true
  }

  return false
}

function collectCallsites(moduleNames) {
  return collectCallsitesFromDirs(moduleNames, SOURCE_DIRS)
}

function collectCallsitesFromDirs(moduleNames, dirs) {
  const moduleSet = new Set(moduleNames)
  const callsites = {}
  for (const moduleName of moduleNames) callsites[moduleName] = []

  for (const dir of dirs) {
    for (const filePath of walkFiles(dir)) {
      const source = fs.readFileSync(filePath, "utf8")
      let match
      while ((match = MODULE_RE.exec(source)) !== null) {
        const moduleName = match[1]
        if (!moduleSet.has(moduleName)) continue
        callsites[moduleName].push(toPosix(filePath))
      }
    }
  }

  for (const moduleName of moduleNames) {
    callsites[moduleName] = [...new Set(callsites[moduleName])].sort()
  }

  return callsites
}

function writeInventory(modules, callsites) {
  return writeInventoryTo(modules, callsites, INVENTORY_PATH, SUMMARY_PATH)
}

function writeInventoryTo(modules, callsites, inventoryPath, summaryPath) {
  const generatedAt = new Date().toISOString()
  const inventory = {
    generatedAt,
    moduleCount: modules.length,
    exportCount: modules.reduce((sum, moduleInfo) => sum + moduleInfo.exports.length, 0),
    modules: modules.map((moduleInfo) => ({
      ...moduleInfo,
      callsites: callsites[moduleInfo.name] || [],
      callsiteCount: (callsites[moduleInfo.name] || []).length,
    })),
  }

  fs.writeFileSync(inventoryPath, `${JSON.stringify(inventory, null, 2)}\n`, "utf8")

  const markdown = [
    "# Data Layer Inventory",
    "",
    `Generated: ${generatedAt}`,
    "",
    `Modules: ${inventory.moduleCount}`,
    `Exports: ${inventory.exportCount}`,
    "",
  ]

  for (const moduleInfo of inventory.modules) {
    markdown.push(`## ${moduleInfo.name}`)
    markdown.push("")
    markdown.push(`- Exports: ${moduleInfo.exports.length}`)
    markdown.push(`- Callsites: ${moduleInfo.callsiteCount}`)
    markdown.push(`- Export names: ${moduleInfo.exports.join(", ")}`)
    if (moduleInfo.callsites.length) {
      markdown.push(`- Files: ${moduleInfo.callsites.join(", ")}`)
    }
    markdown.push("")
  }

  fs.writeFileSync(summaryPath, `${markdown.join("\n").trim()}\n`, "utf8")
}

function main() {
  ensureDir(DATA_DIR)
  ensureDir(SUPABASE_DIR)

  const rtdbFiles = fs
    .readdirSync(RTDB_DIR)
    .filter((name) => /\.(ts|tsx)$/.test(name))
    .sort()

  const modules = rtdbFiles.map((fileName) => {
    const moduleName = path.basename(fileName, path.extname(fileName))
    const filePath = path.join(RTDB_DIR, fileName)
    const source = fs.readFileSync(filePath, "utf8")
    const exportNames = extractExports(source)

    fs.writeFileSync(
      path.join(DATA_DIR, `${moduleName}.ts`),
      createWrapperSource(moduleName, exportNames, {
        firebaseImportPath: `../rtdatabase/${moduleName}`,
        supabaseImportPath: `../providers/supabase/${moduleName}`,
        registryImportPath: "./providerRegistry",
      }),
      "utf8",
    )

    writeGeneratedIfManaged(
      path.join(SUPABASE_DIR, `${moduleName}.ts`),
      createSupabaseStubSource(moduleName, exportNames),
    )

    return {
      name: moduleName,
      file: `app/backend/rtdatabase/${fileName}`,
      exports: exportNames,
      categories: exportNames.reduce((acc, exportName) => {
        acc[exportName] = classifyExport(exportName)
        return acc
      }, {}),
    }
  })

  const moduleNames = modules.map((moduleInfo) => moduleInfo.name)
  const callsites = collectCallsites(moduleNames)

  for (const dir of SOURCE_DIRS) {
    for (const filePath of walkFiles(dir)) {
      rewriteImports(filePath)
    }
  }

  writeInventory(modules, callsites)

  if (fs.existsSync(ADMIN_RTDB_DIR)) {
    ensureDir(ADMIN_DATA_DIR)
    ensureDir(ADMIN_SUPABASE_DIR)

    const adminRtdbFiles = fs
      .readdirSync(ADMIN_RTDB_DIR)
      .filter((name) => /\.(ts|tsx)$/.test(name))
      .sort()

    const adminModules = adminRtdbFiles.map((fileName) => {
      const moduleName = path.basename(fileName, path.extname(fileName))
      const filePath = path.join(ADMIN_RTDB_DIR, fileName)
      const source = fs.readFileSync(filePath, "utf8")
      const exportNames = extractExports(source)

      fs.writeFileSync(
        path.join(ADMIN_DATA_DIR, `${moduleName}.ts`),
        createWrapperSource(moduleName, exportNames, {
          firebaseImportPath: `../rtdatabase/${moduleName}`,
          supabaseImportPath: `../providers/supabase/${moduleName}`,
          registryImportPath: "./providerRegistry",
        }),
        "utf8",
      )

      writeGeneratedIfManaged(
        path.join(ADMIN_SUPABASE_DIR, `${moduleName}.ts`),
        createSupabaseStubSourceWithImports(moduleName, exportNames, {
          firebaseImportPath: `../../rtdatabase/${moduleName}`,
          runtimeImportPath: "./runtime",
        }),
      )

      return {
        name: moduleName,
        file: `admin/backend/rtdatabase/${fileName}`,
        exports: exportNames,
        categories: exportNames.reduce((acc, exportName) => {
          acc[exportName] = classifyExport(exportName)
          return acc
        }, {}),
      }
    })

    const adminCallsites = collectCallsitesFromDirs(
      adminModules.map((moduleInfo) => moduleInfo.name),
      [
        path.join(ROOT, "admin", "backend", "context"),
        path.join(ROOT, "admin", "backend", "functions"),
        path.join(ROOT, "admin", "frontend"),
      ].filter((dir) => fs.existsSync(dir)),
    )

    writeInventoryTo(adminModules, adminCallsites, ADMIN_INVENTORY_PATH, ADMIN_SUMMARY_PATH)
  }

  console.log(`Generated ${modules.length} data facade modules.`)
  console.log(`Inventory written to ${path.relative(ROOT, INVENTORY_PATH)}`)
}

main()
